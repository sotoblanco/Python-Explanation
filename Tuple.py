# tuple
# key difference between the list and tuple is the property of inmutability of the tuppe

t = (1,2,3)

# first element of a tuple is
t[0]

# last element of a tuple
t[-1]

# slicing follows the same rules of the list

t[:3]

t = ("a", "a", "b")

# tuple methods
t.count("a") # counts the number of times an element is on the tuple

t.index("b") # first location of the element inside of a tuple

# you cannot do element assignment on a tuple useful when you don't have the elements changes

